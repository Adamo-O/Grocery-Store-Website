function showDescriptionBread() {
    if (document.getElementById('descContainerBread').hidden == true) {
        document.getElementById('descContainerBread').hidden = false;
        document.getElementById('desButtonBread').innerHTML = 'Show Less';
    }
    else {
        document.getElementById('descContainerBread').hidden=true;
        document.getElementById('desButtonBread').innerHTML = 'More Description';
    }
}
